// 显示骨架屏，该屏幕作为显示模板
// 从上到下分别为：图片（64 * 64 灰度） 文字（128 * 16 单色） 进度条 
void ShowScaffoldScreen(uchar* image, uchar* text, int progress) {
    int image_index = 0;
    int text_index = 0;

    for(int page = 0; page < 16; page++) {
        SetLcdAddress(page, 0);
        // 前 2 page 显示 空数据
        if(page < 2) {
            for(int column = 0; column < 128; column++) {
                TransferDataToLcd(0);
                TransferDataToLcd(0);
            }
        } 
        // 接着 8 page 中间显示 image 
        else if (page < 10) {
            for(int column = 0; column < 128; column++) {
                if (column < 32) {
                    TransferDataToLcd(0);
                    TransferDataToLcd(0);
                } else if (column < 96) {
                    TransferDataToLcd(ReadByteArrayValue(image, image_index++));
                    TransferDataToLcd(ReadByteArrayValue(image, image_index++));
                    // TransferDataToLcd(image[image_index]);
                    // TransferDataToLcd(image[image_index]);
                    // image_index++;
                } else {
                    TransferDataToLcd(0);
                    TransferDataToLcd(0);
                }
            }
        }
        // 接着 2 page 显示 text
        else if (page < 12) {
            for(int column = 0; column < 128; column++) {
                TransferDataToLcd(ReadByteArrayValue(text, text_index));
                TransferDataToLcd(ReadByteArrayValue(text, text_index));
                // TransferDataToLcd(text[text_index]);
                // TransferDataToLcd(text[text_index]);
                text_index++;
            }
        }
        // 接着 1 page 显示 空数据
        else if (page < 13) {
            for(int column = 0; column < 128; column++) {
                TransferDataToLcd(0);
                TransferDataToLcd(0);
            }
        }
        // 接着 1 page 显示 进度条
        else if (page < 14) {
            for(int column = 0; column < 128; column++) {
                if (column < 12) {
                    TransferDataToLcd(0);
                    TransferDataToLcd(0);
                } else if (column < 14) {
                    TransferDataToLcd(0b00011000);
                    TransferDataToLcd(0b00011000);
                    TransferDataToLcd(0b01111110);
                    TransferDataToLcd(0b01111110);
                    column++;
                } else if (column < 114) {
                    if (column - 14 < progress) {
                        TransferDataToLcd(0xff);
                        TransferDataToLcd(0xff);
                    } else {
                        TransferDataToLcd(0b10000001);
                        TransferDataToLcd(0b10000001);
                    }

                } else if (column < 116) {
                    TransferDataToLcd(0b01111110);
                    TransferDataToLcd(0b01111110);
                    TransferDataToLcd(0b00011000);
                    TransferDataToLcd(0b00011000);
                    column++;
                } 
                else {
                    TransferDataToLcd(0);
                    TransferDataToLcd(0);
                }
            }
        }
        // 最后 2 page 显示 空数据
        else {
            for(int column = 0; column < 128; column++) {
                TransferDataToLcd(0);
                TransferDataToLcd(0);
            }
        }
    }
}

